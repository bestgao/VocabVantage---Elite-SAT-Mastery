/**
 * DEEP SANITIZER (HARDENED)
 * 1. Removes everything inside (...)
 * 2. Removes literal "()" signs
 * 3. Removes common CSV garbage strings
 */
export const titanSanitize = (text: string | undefined): string => {
  if (!text) return "";
  return text
    .replace(/\s*\(.*?\)/g, '')   // Remove parenthetical content
    .replace(/\(\s*\)/g, '')      // Remove empty parentheses
    .replace(/\[.*?\]/g, '')      // Remove bracketed content
    .replace(/Related Concept/gi, '')
    .replace(/Another word/gi, '')
    .replace(/Placeholder/gi, '')
    .replace(/Alt:.*$/gi, '')     // Remove "Alt:" notes
    .trim();
};

